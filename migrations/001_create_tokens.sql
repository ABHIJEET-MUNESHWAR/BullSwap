-- Create tokens table
CREATE TABLE IF NOT EXISTS tokens (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    symbol TEXT NOT NULL,
    name TEXT NOT NULL,
    decimals SMALLINT NOT NULL DEFAULT 18,
    address TEXT NOT NULL UNIQUE
);

